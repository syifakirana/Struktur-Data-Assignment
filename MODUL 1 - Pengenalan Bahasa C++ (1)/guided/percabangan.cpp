#include <iostream>
using namespace std;

int main() {
    int angka1 = 10;
    int angka2 = 20;

    if (angka1 > angka2) {
        cout << "Angka 1 lebih besar dari Angka 2" << endl;
    } else if (angka1 < angka2) {
        cout  << "Angka 1 lebih kecil dari Angka 2" << endl;
    } else {
        cout << "Angka 1 sama dengan Angka 2" << endl;
    }

    return 0;
    
}